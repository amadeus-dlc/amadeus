// In-process coverage seam for the #836 Phase Progress roll-up wiring (FR-3).
//
// The defect: only jump (#842) and init wrote the `## Phase Progress` roll-up.
// The advance / finalize / complete-workflow transitions crossed phase
// boundaries and emitted PHASE_COMPLETED/PHASE_VERIFIED/PHASE_STARTED into the
// append-only ledger, but never flipped the roll-up field — so a completed
// bugfix workflow (gate-start → approve → advance, where approve delegates to
// handleAdvance/handleCompleteWorkflow) ended at Status: Completed with the
// roll-up frozen at its init values (Initialization: Active / Construction:
// Pending). The fix flips the roll-up in the SAME content transaction as the
// field/audit updates, mirroring the jump precedent (amadeus-jump.ts).
//
// The handlers are spawn-driven end-to-end elsewhere (t17), which bun's coverage
// cannot see (the spawn blindspot). So this test imports them directly from the
// shipped dist tree (the #761 seam precedent — a stale dist reds here rather
// than passing against un-regenerated bytes) and drives the transitions
// in-process against seeded state. The suite-wide guards are also set here so
// the file passes when run bare.

import { afterEach, beforeEach, describe, expect, test } from "bun:test";
