describe("t-phase-progress-rollup-seam: full bugfix drive leaves no Active/Pending residue (#836 closure)", () => {
  let proj: string;
  let prev: string | undefined;

  beforeEach(() => {
    proj = createTestProject();
    // A faithful bugfix roll-up right after init (with the init fix applied):
    // Initialization Verified, Ideation/Operation Skipped, Inception Active.
    seedStateFile(proj, "state-bugfix-postinit.md");
    prev = process.env.CLAUDE_PROJECT_DIR;
    process.env.CLAUDE_PROJECT_DIR = proj;
  });

  afterEach(() => {
    if (prev === undefined) delete process.env.CLAUDE_PROJECT_DIR;
    else process.env.CLAUDE_PROJECT_DIR = prev;
    cleanupTestProject(proj);
  });

  test("advance through the boundary, then complete-workflow, fully resolves the roll-up", () => {
    handleAdvance(["reverse-engineering"]); // within inception (no boundary)
    handleAdvance(["requirements-analysis"]); // inception → construction
    handleAdvance(["code-generation"]); // within construction (no boundary)
    handleCompleteWorkflow(["build-and-test"]); // terminal → Construction Verified
    const state = readState(proj);
    expect(phaseField(state, "Initialization")).toBe("Verified");
    expect(phaseField(state, "Ideation")).toBe("Skipped");
    expect(phaseField(state, "Inception")).toBe("Verified");
    expect(phaseField(state, "Construction")).toBe("Verified");
    expect(phaseField(state, "Operation")).toBe("Skipped");
    expect(state).toContain("- **Status**: Completed");
    // The #836 headline: no phase is left Active or Pending after completion.
    expect(state).not.toMatch(/^- \*\*(Initialization|Ideation|Inception|Construction|Operation)\*\*: (Active|Pending)$/m);
  });
});

describe("t-phase-progress-rollup-seam: init writes a pre-crossed roll-up in-process (#836 root)", () => {
  let proj: string;
  let prev: string | undefined;

  beforeEach(() => {
    proj = createTestProject();
    prev = process.env.CLAUDE_PROJECT_DIR;
    process.env.CLAUDE_PROJECT_DIR = proj;
  });

  afterEach(() => {
    if (prev === undefined) delete process.env.CLAUDE_PROJECT_DIR;
    else process.env.CLAUDE_PROJECT_DIR = prev;
    cleanupTestProject(proj);
  });

  test("bugfix birth stamps Initialization Verified (not Active) and Inception Active", () => {
    // Drive the real birth handler in-process (t39/t20 spawn it, which bun's
    // coverage cannot see — this seam registers the init phaseStatus wiring in
    // lcov). Birth pre-crosses initialization → inception for bugfix.
    handleIntentBirth(proj, { scope: "bugfix" });
    const state = readFileSync(bornStateFile(proj), "utf-8");
    expect(phaseField(state, "Initialization")).toBe("Verified");
    expect(phaseField(state, "Ideation")).toBe("Skipped");
    expect(phaseField(state, "Inception")).toBe("Active");
    expect(phaseField(state, "Construction")).toBe("Pending");
    expect(phaseField(state, "Operation")).toBe("Skipped");
    // The #836 headline at init time: initialization is NOT left Active.
    expect(state).not.toContain("- **Initialization**: Active");
  });
});

describe("t-phase-progress-rollup-seam: initPhaseStatus pure seam (#836 init pre-cross)", () => {
  // Init pre-crosses initialization → the first post-init phase. firstPostInit
  // in a later phase (the common case): initialization Verified, that phase
  // Active, others Pending/Skipped.
  test("crossed: initialization → Verified", () => {
    expect(initPhaseStatus("initialization", "inception", false)).toBe("Verified");
  });
  test("crossed: first post-init phase → Active", () => {
    expect(initPhaseStatus("inception", "inception", true)).toBe("Active");
  });
  test("crossed: later phase with EXECUTE work → Pending", () => {
    expect(initPhaseStatus("construction", "inception", true)).toBe("Pending");
  });
  test("crossed: phase with no EXECUTE work → Skipped", () => {
    expect(initPhaseStatus("operation", "inception", false)).toBe("Skipped");
  });
  test("not crossed (firstPostInit null): initialization stays Active", () => {
    expect(initPhaseStatus("initialization", null, false)).toBe("Active");
  });
});
