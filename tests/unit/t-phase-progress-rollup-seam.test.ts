import { join } from "node:path";
import {
  handleAdvance,
  handleCompleteWorkflow,
  handleFinalize,
} from "../../dist/claude/.claude/tools/amadeus-state.ts";
import {
  handleIntentBirth,
  initPhaseStatus,
} from "../../dist/claude/.claude/tools/amadeus-utility.ts";
import {
  cleanupTestProject,
  createTestProject,
  seededStateFile,
  seedStateFile,
} from "../harness/fixtures.ts";

// Resolve the born intent's state file from the active-intent cursor (birth
// mints a NEW intent; the seeded default record is not it).
function bornStateFile(proj: string): string {
  const intentsDir = join(proj, "amadeus", "spaces", "default", "intents");
  const cursor = join(intentsDir, "active-intent");
  const rec = existsSync(cursor) ? readFileSync(cursor, "utf-8").trim() : "";
  return join(intentsDir, rec, "amadeus-state.md");
}

process.env.AMADEUS_SKIP_ARTIFACT_GUARD ??= "1";
process.env.AMADEUS_SKIP_HUMAN_PRESENCE_GUARD ??= "1";

function readState(proj: string): string {
  return readFileSync(seededStateFile(proj), "utf-8");
}

// Extract a `- **<Phase>**: <status>` roll-up value, or "(missing)".
function phaseField(state: string, phase: string): string {
  const m = state.match(new RegExp(`^- \\*\\*${phase}\\*\\*: (\\w+)$`, "m"));
  return m ? m[1] : "(missing)";
}

describe("t-phase-progress-rollup-seam: advance across a phase boundary (#836)", () => {
  let proj: string;
  let prev: string | undefined;

  beforeEach(() => {
    proj = createTestProject();
    // scope=bugfix, Current Stage=requirements-analysis (inception, Active);
    // reverse-engineering already [x]. Advancing derives code-generation
    // (construction) → an inception→construction boundary crossing.
    seedStateFile(proj, "state-mid-inception.md");
    prev = process.env.CLAUDE_PROJECT_DIR;
    process.env.CLAUDE_PROJECT_DIR = proj;
  });

  afterEach(() => {
    if (prev === undefined) delete process.env.CLAUDE_PROJECT_DIR;
    else process.env.CLAUDE_PROJECT_DIR = prev;
    cleanupTestProject(proj);
  });

  test("flips the closed phase → Verified and the entered phase → Active", () => {
    // Pre-state: Inception Active, Construction Pending (the un-wired bug froze
    // these). Single-arg advance derives the next in-scope stage.
    handleAdvance(["requirements-analysis"]);
    const state = readState(proj);
    expect(phaseField(state, "Inception")).toBe("Verified");
    expect(phaseField(state, "Construction")).toBe("Active");
    // Untouched phases keep their prior roll-up (per the seed fixture).
    expect(phaseField(state, "Initialization")).toBe("Verified");
    expect(phaseField(state, "Ideation")).toBe("Verified");
    expect(phaseField(state, "Operation")).toBe("Pending");
  });
});

describe("t-phase-progress-rollup-seam: complete-workflow verifies the final phase (#836 main repro)", () => {
  let proj: string;
  let prev: string | undefined;

  beforeEach(() => {
    proj = createTestProject();
    // scope=bugfix at the final construction stage (build-and-test [-]),
    // Construction Active. complete-workflow must flip it Verified in lock-step
    // with Status: Completed — the direct closure of the #836 symptom.
    seedStateFile(proj, "state-bugfix-final-construction.md");
    prev = process.env.CLAUDE_PROJECT_DIR;
    process.env.CLAUDE_PROJECT_DIR = proj;
  });

  afterEach(() => {
    if (prev === undefined) delete process.env.CLAUDE_PROJECT_DIR;
    else process.env.CLAUDE_PROJECT_DIR = prev;
    cleanupTestProject(proj);
  });

  test("Construction → Verified and Status → Completed in the same transaction", () => {
    handleCompleteWorkflow(["build-and-test"]);
    const state = readState(proj);
    expect(phaseField(state, "Construction")).toBe("Verified");
    expect(state).toContain("- **Status**: Completed");
    // No residual Active/Pending anywhere in the roll-up (the #836 assertion).
    for (const p of ["Initialization", "Ideation", "Inception", "Construction", "Operation"]) {
      expect(["Verified", "Skipped"]).toContain(phaseField(state, p));
    }
  });
});

